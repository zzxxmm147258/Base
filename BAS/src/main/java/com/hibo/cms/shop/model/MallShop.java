public class MallShop {
	
	    private String district;//商圈编号

	    private String disname;//商圈名称

	    private String city;//商圈城市
	    
	    private String blockNo;//街区编号
	    
	    private String blockName;//街区名称
	    
	    private String buildingNo;//单元编号
	    
	    private String buildingName;//单元名称
	    
	    private String floorNo;//楼层编号

	    private String floorName;//楼层名称
	    
	    private String shopAddress;//商户地址
	    
	    private String shopType;//商户类型
	    
	    private String shopname;//商户名称

		public String getDistrict() {
			return district;
		}

		public void setDistrict(String district) {
			this.district = district;
		}

		public String getDisname() {
			return disname;
		}

		public void setDisname(String disname) {
			this.disname = disname;
		}

		public String getCity() {
			return city;
		}

		public void setCity(String city) {
			this.city = city;
		}

		public String getBlockNo() {
			return blockNo;
		}

		public void setBlockNo(String blockNo) {
			this.blockNo = blockNo;
		}

		public String getBlockName() {
			return blockName;
		}

		public void setBlockName(String blockName) {
			this.blockName = blockName;
		}

		public String getBuildingNo() {
			return buildingNo;
		}

		public void setBuildingNo(String buildingNo) {
			this.buildingNo = buildingNo;
		}

		public String getBuildingName() {
			return buildingName;
		}

		public void setBuildingName(String buildingName) {
			this.buildingName = buildingName;
		}

		public String getFloorNo() {
			return floorNo;
		}

		public void setFloorNo(String floorNo) {
			this.floorNo = floorNo;
		}

		public String getFloorName() {
			return floorName;
		}

		public void setFloorName(String floorName) {
			this.floorName = floorName;
		}

		public String getShopAddress() {
			return shopAddress;
		}

		public void setShopAddress(String shopAddress) {
			this.shopAddress = shopAddress;
		}

		public String getShopType() {
			return shopType;
		}

		public void setShopType(String shopType) {
			this.shopType = shopType;
		}

		public String getShopname() {
			return shopname;
		}

		public void setShopname(String shopname) {
			this.shopname = shopname;
		}
}
